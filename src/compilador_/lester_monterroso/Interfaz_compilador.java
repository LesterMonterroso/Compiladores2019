 public String GuardarATexto(File nuevo,String contenido){
        String respuesta=null;
        try{
            guardar = new FileOutputStream(nuevo);
            byte[] bytesTxt=contenido.getBytes();
            guardar.write(bytesTxt);
            respuesta="Archivo guardado con Exito";
        }catch (Exception e){
            
        }
        return respuesta;
    }
